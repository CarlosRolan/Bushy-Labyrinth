// Tama√±o de cada celda del laberinto
const cellSize = 2;

function initMaze() {
  const mazeGroup = new THREE.Group();

  for (let i = 0; i < mazeData.length; i++) {
    for (let j = 0; j < mazeData[i].length; j++) {
      if (mazeData[i][j] === 1) {
        const wallGeometry = new THREE.BoxGeometry(
          cellSize,
          cellSize,
          cellSize
        );
        const wallMaterial = new THREE.MeshBasicMaterial({ color: 0x555555 });
        const wall = new THREE.Mesh(wallGeometry, wallMaterial);
        wall.position.set(
          (j - mazeData.length / 2) * cellSize,
          0,
          (i - mazeData.length / 2) * cellSize
        );
        mazeGroup.add(wall);
      }
    }
  }

  return mazeGroup;
}

const maze = initMaze();

